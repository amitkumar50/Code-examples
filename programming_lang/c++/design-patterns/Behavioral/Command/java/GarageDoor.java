public class GarageDoor
{
	public void open()
	{
		System.out.println("Garage Door open");
	}
	public void closed()
	{
		System.out.println("Garage Door Closed");
	}
	public void stop()
	{
		System.out.println("Door stop");
	}
	public void lightOn()
	{
		System.out.println("Door lightOn");
	}
	public void lightOff()
	{
		System.out.println("Door lightOff");
	}
}
